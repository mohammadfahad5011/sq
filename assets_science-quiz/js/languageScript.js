// Function to change the lang
function changelang(lang) {
  //Hero content
  //   document.querySelector(".hero_subtitle").textContent =
  //     langData[lang].heroSubtitle;
  //   document.querySelector(".hero_main-title").textContent =
  //     langData[lang].heroMainTitle;
  document.querySelector(".sponsore-title").textContent =
    langData[lang].sponsoreTitle;

  const bnWrapper = document.querySelector(".bn-wrapper");
  const enWrapper = document.querySelector(".en-wrapper");

  if (lang === "bn") {
    bnWrapper.style.display = "block";
    enWrapper.style.display = "none";
  } else {
    bnWrapper.style.display = "none";
    enWrapper.style.display = "block";
  }

  // Notice content
  document.querySelector(".notice-content").innerHTML =
    langData[lang].noticeContent;

  //essay-content

  document.querySelector(".essay-content").innerHTML =
    langData[lang].essayContent;

  // quizzes-caution-content
  document.querySelector(".quizzes-caution-content").innerHTML =
    langData[lang].quizzesCautionContent;

  //announcement
  document.querySelector(".announcement-content").innerHTML =
    langData[lang].announcementContent;

  //pricebond
  document.querySelector(".pricebond-content").innerHTML =
    langData[lang].pricebondContent;

  //pattner
  document.querySelector(".pattner-section h2").textContent =
    langData[lang].mainTitle;

  // Font change based on language
  if (lang === "bn") {
    document.body.style.fontFamily = "var(--primary-font)";
  } else {
    // Select all heading elements (h1, h2, h3, h4, h5, h6)
    document
      .querySelectorAll("h1, h2, h3, h4, h5, h6")
      .forEach(function (element) {
        element.style.fontFamily = "degular";
        element.style.fontWeight = "700";
      });

    // Select all paragraph-related elements (p, a, ul, li ,sapn)
    document
      .querySelectorAll("p, a, ul, li, span,button ")
      .forEach(function (element) {
        element.style.fontFamily = "degular";
        element.style.fontWeight = "400";
      });
  }

  // Update active class
  document
    .querySelectorAll(".lang div")
    .forEach((div) => div.classList.remove("active"));
  document.getElementById(lang).classList.add("active");
}

// Add event listeners for lang switching
document.getElementById("bn").addEventListener("click", () => changelang("bn"));
document.getElementById("en").addEventListener("click", () => changelang("en"));

// Set default lang (English)
changelang("bn");
